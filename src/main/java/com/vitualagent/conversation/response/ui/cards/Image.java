package com.vitualagent.conversation.response.ui.cards;

import com.vitualagent.conversation.response.ui.Action;



public class Image extends CardElement{
	
	
	private String altText;
	
	private String horizontalAlignment;
	
	private String size;
	
	private String style;
	
	private String url;
	
	private Action selectAction;
	
	private String height;
	
	private String width;

	public String getAltText() {
		return altText;
	}

	public void setAltText(String altText) {
		this.altText = altText;
	}

	public String getHorizontalAlignment() {
		return horizontalAlignment;
	}

	public void setHorizontalAlignment(String horizontalAlignment) {
		this.horizontalAlignment = horizontalAlignment;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getStyle() {
		return style;
	}

	public void setStyle(String style) {
		this.style = style;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Action getSelectAction() {
		return selectAction;
	}

	public void setSelectAction(Action selectAction) {
		this.selectAction = selectAction;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}
	
	
	
	

}
